def parse_template(template: str) -> List[List[str]]:
    """
    Разбирает шаблон с вариантами в фигурных скобках.
    Возвращает список списков: для каждой позиции — список возможных фраз.
    """
    import re
    parts = []
    # Ищем все группы {a|b|c}
    last_end = 0
    for match in re.finditer(r'\{([^}]+)\}', template):
        # Текст до группы
        if match.start() > last_end:
            parts.append([template[last_end:match.start()]])
        # Варианты внутри {}
        options = match.group(1).split('|')
        parts.append(options)
        last_end = match.end()
    # Остаток после последней группы
    if last_end < len(template):
        parts.append([template[last_end:]])
    return parts

def generate_messages(parts: List[List[str]]) -> List[str]:
    """
    Генерирует все возможные сообщения из размеченного шаблона.
    Внимание: при большом числе вариантов (~2^32) это невозможно.
    Здесь для демонстрации используется ограниченное число вариантов.
    """
    # Подсчитываем общее число комбинаций
    num_combinations = 1
    for p in parts:
        num_combinations *= len(p)
    
    print(f"  Всего возможных сообщений: {num_combinations:,}")
    
    # Для демонстрации ограничимся первыми 10,000 (или меньше)
    max_samples = min(10000, num_combinations)
    messages = []
    
    # Используем itertools.product для перебора
    # Но если комбинаций слишком много, берем только первые max_samples
    count = 0
    for combination in itertools.product(*parts):
        msg = ''.join(combination)
        messages.append(msg)
        count += 1
        if count >= max_samples:
            break
    
    return messages

def hash_message(msg: str, hash_algo = 'sha256') -> str:
    """Вычисляет хеш сообщения (возвращает hex-строку)."""
    return hashlib.new(hash_algo, msg.encode('utf-8')).hexdigest()

def birthday_attack(messages_A: List[str], messages_B: List[str], bits = 64) -> Tuple[str, str, str]:
    """
    Атака "дней рождения": ищет коллизию между двумя наборами сообщений.
    bits — сколько бит хеша использовать (обрезаем хеш до первых bits бит).
    Возвращает (msg_A, msg_B, colliding_hash_prefix)
    """
    # Словарь: хеш-префикс -> сообщение (из набора A)
    hash_map: Dict[str, str] = {}
    
    # Обрезаем хеш до bits/4 hex-символов (т.к. 1 hex = 4 бита)
    hex_len = bits // 4
    
    print(f"\nИщем коллизию по первым {bits} битам хеша ({hex_len} hex-символов)")
    print(f"Размер набора A: {len(messages_A):,}, набора B: {len(messages_B):,}")
    print(f"Ожидаемое число пар для коллизии: ~{2 ** (bits / 2):.0e}")
    
    # Заполняем словарь из набора A
    for i, msg in enumerate(messages_A):
        full_hash = hash_message(msg)
        prefix = full_hash[:hex_len]
        if prefix not in hash_map:
            hash_map[prefix] = msg
        if (i + 1) % 1000 == 0:
            print(f"  Обработано {i + 1} сообщений из A...")
    
    # Ищем коллизию в наборе B
    for i, msg in enumerate(messages_B):
        full_hash = hash_message(msg)
        prefix = full_hash[:hex_len]
        if prefix in hash_map:
            print(f"\nКоллизия найдена!")
            print(f"Хеш-префикс: {prefix}")
            print(f"Сообщение A (хорошее): {hash_map[prefix][:200]}...")
            print(f"Сообщение B (плохое): {msg[:200]}...")
            return hash_map[prefix], msg, prefix
        if (i + 1) % 1000 == 0:
            print(f"  Проверено {i + 1} сообщений из B...")
    
    print("Коллизия не найдена — попробуйте увеличить количество вариантов или уменьшить bits")
    return None, None, None

