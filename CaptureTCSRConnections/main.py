# Проверка ОС и импорт Scapy
try:
    from scapy.all import *
    from scapy.arch import get_if_list
    SCAPY_AVAILABLE = True
except ImportError:
    SCAPY_AVAILABLE = False
    print("[!] Scapy not installed. Install with: pip install scapy")

def check_permissions():
    """Проверка прав администратора / root"""
    if platform.system() == "Windows":
        try:
            import ctypes
            return ctypes.windll.shell32.IsUserAnAdmin() != 0
        except:
            return False
    else:
        try:
            return os.geteuid() == 0
        except:
            return False

def get_working_interface():
    """Получить работающий сетевой интерфейс"""
    if not SCAPY_AVAILABLE:
        return None
    
    try:
        if platform.system() == "Windows":
            from scapy.arch.windows import get_windows_if_list
            ifaces = get_windows_if_list()
            for iface in ifaces:
                if iface.get('ips') and '127.0.0.1' not in str(iface.get('ips')):
                    return iface.get('name')
        else:
            ifaces = get_if_list()
            for iface in ifaces:
                if iface not in ['lo', 'Loopback']:
                    return iface
    except:
        pass
    return None

